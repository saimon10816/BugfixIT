            'contactEmail' => 'required|email',
            'contactSubject' => 'required|string',
            'contactMessage' => 'required',
            ]);

        Mail::to('bugfixitbd@gmail.com')->send(new ContactFormMail($contact));

        return redirect('/#page7')->with('Success', 'Your Inquiry has been sent successfully.');

